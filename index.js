
function add(a, b){
    let sum = a + b;
    return sum
}
function subtract(a, b){
    let sub = a -b;
    return sub
}
function multiply(a, b){
    let mult = a * b;
    return mult
}
function divide(a, b){
    let dev = a / b;
    return dev
}
function increment(n){
    let inc = n;
    inc++
    return inc
}
function decrement(n){
    let dec = n;
    dec--
    return dec
}
